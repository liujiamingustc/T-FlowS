!==============================================================================!
  subroutine Allocate_Grid_Nodes(grid, n)
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  type(Grid_Type) :: grid
  integer         :: n
!==============================================================================!

  allocate(grid % nodes(n))

  grid % nodes(1:n) % x = 0.0 
  grid % nodes(1:n) % y = 0.0 
  grid % nodes(1:n) % z = 0.0 

  end subroutine
