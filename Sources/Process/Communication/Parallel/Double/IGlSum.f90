!======================================================================!
  subroutine IGlSum(phi) 
!----------------------------------------------------------------------!
!   Estimates global summ among all processors.                        !
!----------------------------------------------------------------------!
  implicit none
!------------------------------[Include]-------------------------------!
  include 'mpif.h'
!-----------------------------[Arguments]------------------------------!
  integer :: phi
!-------------------------------[Locals]-------------------------------!
  integer :: phi_new
  integer :: error
!======================================================================!

!================================================
      call MPI_ALLREDUCE      &               
!-----------------------------------+------------
             (phi,            & ! send buffer
              phi_new,        & ! recv buffer 
              1,              & ! length     
              MPI_INTEGER,    & ! datatype  
              MPI_SUM,        & ! operation 
              MPI_COMM_WORLD, &             
              error) 
!================================================

  phi = phi_new

  end subroutine IGlSum
