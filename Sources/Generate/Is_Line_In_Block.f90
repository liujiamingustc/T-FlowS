!==============================================================================!
  integer function Is_Line_In_Block(n1, n2, b) 
!------------------------------------------------------------------------------!
!   Checks if the line defined n1 and n2 is inside the block b.                !
!------------------------------------------------------------------------------!
!----------------------------------[Modules]-----------------------------------!
  use all_mod
  use gen_mod
  use Block_Mod
!------------------------------------------------------------------------------! 
  implicit none
!---------------------------------[Arguments]----------------------------------!
  integer :: b, n1, n2
!-----------------------------------[Locals]-----------------------------------!
  integer :: l1, l2
!==============================================================================!

  do l1=1,8
    do l2=1,8
      if( (blocks(b) % points(l1) == n1) .and.   &
          (blocks(b) % points(l2) == n2) ) then
           goto 1
      end if 
    end do
  end do     

  Is_Line_In_Block=0
  return

1 Is_Line_In_Block=b
  return

  end function Is_Line_In_Block
