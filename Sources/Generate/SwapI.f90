!======================================================================!
  subroutine SwapI(a,b) 
!----------------------------------------------------------------------!
!   Swaps two integers.                                                !
!----------------------------------------------------------------------!
  implicit none
!-----------------------------[Parameters]-----------------------------!
  integer :: a,b  
!-------------------------------[Locals]-------------------------------!
  integer :: t
!======================================================================!

  t=a
  a=b
  b=t

  end subroutine SwapI
