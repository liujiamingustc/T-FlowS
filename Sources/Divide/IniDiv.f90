!======================================================================!
  subroutine IniDiv 
!----------------------------------------------------------------------!
!   Initialize processor numbers to 1                                  !
!----------------------------------------------------------------------!
!------------------------------[Modules]-------------------------------!
  use all_mod
  use par_mod 
  use div_mod 
!----------------------------------------------------------------------!
  implicit none
!-------------------------------[Locals]-------------------------------!
  integer :: c
!======================================================================!

!------------------------------!
!     Initialize parameters    !
!------------------------------!
  COORDINATE = 1
  INERTIAL   = 2

!------------------------------!
!     Initialize processors    !
!------------------------------!
  do c=1,NC
    proces(c)=1
  end do

  end subroutine IniDiv
